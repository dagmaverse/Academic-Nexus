// Utility functions for working with textbook data
export function getTextbooksByGrade(grade) {
  return textbooksData[grade] || [];
}

export function getAllTextbooks() {
  return Object.values(textbooksData).flat();
}

export function searchTextbooks(query) {
  const allTextbooks = getAllTextbooks();
  const searchTerm = query.toLowerCase().trim();

  return allTextbooks.filter(
    (textbook) =>
      textbook.subject.toLowerCase().includes(searchTerm) ||
      textbook.description.toLowerCase().includes(searchTerm) ||
      textbook.grade.includes(searchTerm) ||
      (textbook.tags &&
        textbook.tags.some((tag) => tag.toLowerCase().includes(searchTerm)))
  );
}

export function getTextbooksBySubject(subject) {
  const allTextbooks = getAllTextbooks();
  return allTextbooks.filter((textbook) => textbook.subject === subject);
}

export function getPopularTextbooks(limit = 10) {
  const allTextbooks = getAllTextbooks();
  return allTextbooks.sort((a, b) => b.downloads - a.downloads).slice(0, limit);
}
